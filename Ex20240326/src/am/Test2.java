package am;

public class Test2 {

	public static void main(String[] args) {
		// 대입연산자
		//  = : 우측의 값을 좌측변수에 저장
		// += : 우측의 값을 좌측변수에 더하면서 저장
		// -= : 우측의 값을 좌측변수에 빼면서 저장
		// *= : 우측의 값을 좌측변수에 곱하면서 저장
		// /= : 우측의 값을 좌측변수에 나누면서 저장
		// %= : 우측의 값을 좌측변수에 나머지값을 구해서 저장
		
		int a = 10;
		int b = 7;
		
		a += b; // a = a+b;
		System.out.println("a: "+a+", b: "+b);

	}

}
